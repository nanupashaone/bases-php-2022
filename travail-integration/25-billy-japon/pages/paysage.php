    <h1>Les cerisiers du japon</h1>
   <p>Les sakura (桜 / 櫻 / さくら?) sont les cerisiers ornementaux du Japon (dont Prunus serrulata) ainsi que leurs fleurs. La cerise (桜んぼ, sakuranbo?) vient d'une autre espèce de Prunus.

Les premiers sakura sont originaires de Chine et ont été aperçus pour la première fois au pied de l’Himalaya. Ce n’est qu’un millier d’années après, pendant la dynastie Tang, que les cerisiers ont commencé à être exporté au Japon et en Corée du Sud.

Ces arbres sont beaucoup associés au Japon du fait de la forte présence culturelle des sakura dans la culture japonaise et qu’on leur attribue la dénomination de « sakura », qui est l’appellation japonaise. Mais leurs origines sont souvent controversés.</p>



