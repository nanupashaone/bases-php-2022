    <h1>Les mangas</h1>

    <p>Un manga (漫画?) est une bande dessinée japonaise. La plupart des mangas se conforment à un style développé au Japon à la fin du xixe siècle, alors que la forme a une longue préhistoire dans l'art japonais antérieur. Au Japon, le terme manga désigne plus généralement la bande dessinée, quelle que soit son origine. En Occident, le mot « manga » désigne une bande dessinée japonaise, mais aussi parfois une bande dessinée non japonaise respectant les codes des productions populaires japonaises ou pour nommer, par métonymie, d'autres produits visuels rappelant certaines de ces bandes dessinées (dessins animés, style graphique, etc.).

Les mangas se lisent généralement de droite à gauche. En raison du rythme élevé de parution1 et pour limiter le coût d'impression, la plupart des mangas sont dessinés en noir et blanc, mis à part la couverture. Les deux premiers pays les plus consommateurs de mangas sont le Japon et la France2,3,4,5,6. La personne réalisant des mangas est appelée mangaka.</p>

    




